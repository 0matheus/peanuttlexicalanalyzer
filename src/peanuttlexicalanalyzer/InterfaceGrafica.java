    private void analisarLexicamente() throws IOException {
        jTextArea1.setText("");
        File arquivo;
        if (!isLerArquivo) {
            arquivo = new File("arquivo.txt");
            PrintWriter writer;
            try {
                writer = new PrintWriter(arquivo);
                writer.print(jTextField1.getText());
                writer.close();
            } catch (IOException e) {
                Logger.getLogger(InterfaceGrafica.class.getName()).log(Level.SEVERE, null, e);
            }
        } else {
            arquivo = chooser.getSelectedFile().getAbsoluteFile();
        }

        Reader reader = new BufferedReader(new FileReader(arquivo));
        Lexer lexer = new Lexer(reader);
        String resultado = "";
        while (true) {
            PeanuttToken token = lexer.yylex();
            if (token == null) {
                resultado = resultado + "EOF";
                jTextArea1.setText(resultado);
                return;
            }

            switch (token) {
                case ERROR:
                    resultado = resultado + "Erro, S√≠mbolo n√£o encontrado\n";
                    break;
                case IGUAL:
                case SOMA:
                case MULT:
                case MENOS:
                case DIV:
                case ID:
                case INT:
                case NAME:
                case VALUE:
                case LINE:
                case COLUMN:
                case IF:
                case ELSE:
                case SWITCH:
                case ENDIF:
                case THEN:
                case WHILE:
                case FOR:
                case ENDFOR:
                case INTEIRO:
                case PALAVRA_RESERVADA:
                case CARACTER:
                case SIMBOLO:
                case REAL:
                case STRING:
                case MODULO:
                case SINGLEQUOTE:
                case DOUBLEQUOTE:
                case SET_OF_CARACTERS:
                case DIFF:
                case LESSTHEN:
                case LESSTHENEQUALS:
                case GREATERTHEN:
                case GREATERTHENEQUALS:
                case BOOLEAN:
                case SEPARATOR:
                case OBLOCK:
                case CBLOCK:
                case CODE:
                case END_CODE:
                case RECEBE:
                    resultado =  resultado + " TOKEN: " + token +" "+ lexer.lexeme + "\n";
                    break;
                default:
                    resultado = resultado + " TOKEN: " + token + "\n";
            }
        }
    }

    private void escolherArquivo() {
        chooser = new JFileChooser();
        FileNameExtensionFilter filter = new FileNameExtensionFilter("Arquivos .pen ou .txt", "txt", "pen");
        chooser.setFileFilter(filter);
        int returnVal = chooser.showOpenDialog(null);
        if (returnVal == JFileChooser.APPROVE_OPTION) {
            System.out.println("You chose to open this file: " + chooser.getSelectedFile().getName());
        }
    }

